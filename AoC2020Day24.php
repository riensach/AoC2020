foreach($inputArray as $key => $puzzleValue) {
    preg_match_all("/(se)|(sw)|(ne)|(nw)|(e)|(w)/", $puzzleValue, $directions);
    //echo "$puzzleValue<br>";
    //var_dump($directions[0]);
    $x = 0;
